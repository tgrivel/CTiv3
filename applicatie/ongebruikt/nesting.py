def get_balans_nested(x):
    balans          = [r for r in x if r['rekeningkant'] == 'balans']
    balans_nested   = dict()

    for row in balans:
        balanscode  = row['balanscode']
        standpr     = row['standper']
        bedrag      = row['bedrag']

        if balanscode not in balans_nested:
            balans_nested[balanscode] = dict()

        if standpr not in balans_nested[balanscode]:
            balans_nested[balanscode][standpr] = dict()

        balans_nested[balanscode][standpr] = bedrag

    return balans_nested


